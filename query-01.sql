CREATE TABLE Usuarios(
	UsuarioID UNIQUEIDENTIFIER NOT NULL PRIMARY KEY DEFAULT NEWID(),
	UserName NVARCHAR(30) UNIQUE NOT NULL,
	ShowName NVARCHAR(40) NOT NULL,
	Email NVARCHAR(50) UNIQUE NOT NULL,
	BirthDate DATETIME2 NOT NULL,
	Password NVARCHAR(20) NOT NULL,
	CreatedAt DATETIME2 NOT NULL DEFAULT SYSUTCDATETIME(),
	DeletedAt DATETIME2 NULL,
);

CREATE TABLE Canal(
	CanalID UNIQUEIDENTIFIER NOT NULL PRIMARY KEY DEFAULT NEWID(),
	UsuarioID UNIQUEIDENTIFIER NOT NULL REFERENCES Usuarios(UsuarioID),
	ShowName NVARCHAR(30) NOT NULL,
	UserCanal NVARCHAR(30) UNIQUE NOT NULL,
	Verificacion BIT NOT NULL DEFAULT (0),
	Descripcion NVARCHAR(255) NULL,
	AvatarURL NVARCHAR(255) NULL,
	BannerURL NVARCHAR(255) NULL,
	CreatedAt DATETIME2 NOT NULL DEFAULT SYSUTCDATETIME(),
	DeletedAt DATETIME2 NULL,
);

CREATE TABLE Notificacion(
	NotificacionID UNIQUEIDENTIFIER NOT NULL PRIMARY KEY DEFAULT NEWID(),
	Titulo NVARCHAR(30) NOT NULL,
	Contenido NVARCHAR(100) NOT NULL,
	MiniaturaURL NVARCHAR(255),
	Interaccion BIT NOT NULL DEFAULT (0),
);

CREATE TABLE VideosAccesibilidad(
	VideosAccesibilidadID INT IDENTITY(1, 1) NOT NULL PRIMARY KEY,
	Nombre NVARCHAR(30) NOT NULL,
	CreatedAt DATETIME2 NOT NULL DEFAULT SYSUTCDATETIME(),
);

